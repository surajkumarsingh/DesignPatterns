package externalBuilderClass;

public class Employee {

	private String name;
	private String job;
	private int age;

	public String getName() {
		return name;
	}

	public String getJob() {
		return job;
	}

	public int getAge() {
		return age;
	}

	public Employee(String name, String job, int age) {
		this.name = name;
		this.job = job;
		this.age = age;

	}

}
